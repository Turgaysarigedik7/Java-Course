package Operations;

public class Operation2 {
    public static void main(String[] args) {
        int sayi1 = 10;
        sayi1 += 2;
        // += önce toplama işlemini yap sonra sonucu ATAMA yap!

        System.out.println(sayi1);

        int sayi2 = 10;
        sayi2 -= 2;
        // += önce toplama işlemini yap sonra sonucu ATAMA yap!

        System.out.println(sayi2);

        int sayi3= 20;
        sayi3++;
        System.out.println(sayi3);

        sayi3--;
        System.out.println(sayi3);
    }
}
